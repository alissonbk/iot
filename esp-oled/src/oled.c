/* ════════════════════════════════════════════════════════════════════════════
   Low-level helpers
   ════════════════════════════════════════════════════════════════════════════ */

static void oled_send_cmd(const uint8_t *cmds, size_t len)
{
    uint8_t buf[64];
    buf[0] = 0x00;
    memcpy(&buf[1], cmds, len);
    ESP_ERROR_CHECK(i2c_master_transmit(oled_handle, buf, len + 1, -1));
}

void oled_flush(void)
{
    for (uint8_t page = 0; page < 8; page++) {

        uint8_t cmds[] = {
            (uint8_t)(0xB0 + page),
            0x02,
            0x10
        };

        oled_send_cmd(cmds, sizeof(cmds));

        uint8_t buf[129];
        buf[0] = 0x40;
        memcpy(&buf[1], &fb[page * OLED_WIDTH], OLED_WIDTH);

        ESP_ERROR_CHECK(i2c_master_transmit(oled_handle, buf, 129, -1));
    }
}

/* ════════════════════════════════════════════════════════════════════════════
   Frame-buffer drawing helpers
   ════════════════════════════════════════════════════════════════════════════ */

void fb_clear(void) { memset(fb, 0, sizeof(fb)); }

static int fb_draw_char(int x, int page, char c)
{
    if (c < 0x20 || c > 0x7E) c = '?';
    const uint8_t *glyph = font5x7[c - 0x20];

    for (int col = 0; col < 5; col++) {
        int px = x + col;
        if (px >= OLED_WIDTH) break;
        fb[page * OLED_WIDTH + px] = glyph[col];
    }
    if (x + 5 < OLED_WIDTH)
        fb[page * OLED_WIDTH + x + 5] = 0x00;

    return x + 6;
}

void fb_draw_string(int x, int page, const char *str)
{
    while (*str && page < OLED_PAGES) {
        if (x + 6 > OLED_WIDTH) { x = 0; page++; }
        x = fb_draw_char(x, page, *str++);
    }
}

void fb_draw_bitmap(int x, int y, int w, int h, const uint8_t *bitmap)
{
    int start_page = y / 8;
    int bit_off    = y % 8;
    int bmp_pages  = (h + 7) / 8;

    for (int col = 0; col < w; col++) {
        int px = x + col;
        if (px < 0 || px >= OLED_WIDTH) continue;

        for (int p = 0; p < bmp_pages; p++) {
            int tp = start_page + p;
            if (tp < 0 || tp >= OLED_PAGES) continue;

            uint8_t src = bitmap[col + p * w];

            if (bit_off == 0) {
                fb[tp * OLED_WIDTH + px] |= src;
            } else {
                fb[tp * OLED_WIDTH + px] |= (uint8_t)(src << bit_off);
                if (tp + 1 < OLED_PAGES)
                    fb[(tp + 1) * OLED_WIDTH + px] |= (uint8_t)(src >> (8 - bit_off));
            }
        }
    }
}

void fb_draw_fullscreen(const uint8_t *bitmap)
{
    memcpy(fb, bitmap, sizeof(fb));
}

/* ════════════════════════════════════════════════════════════════════════════
   SSD1306 initialisation sequence
   ════════════════════════════════════════════════════════════════════════════ */

void oled_init(void)
{
    static const uint8_t init_seq[] = {
        0xAE,
        0xD5, 0x80,
        0xA8, 0x3F,
        0xD3, 0x00,
        0x40,
        0x8D, 0x14,
        0x20, 0x00,
        0xA1,
        0xC8,
        0xDA, 0x12,
        0x81, 0xCF,
        0xD9, 0xF1,
        0xDB, 0x40,
        0xA4,
        0xA6,
        0xAF,
    };
    ESP_LOGI(TAG, "Initialising I2C …");
    i2c_master_init();

    ESP_LOGI(TAG, "Initialising SSD1306 OLED …");
    oled_send_cmd(init_seq, sizeof(init_seq));    
    fb_clear();
    oled_flush();    
    vTaskDelay(pdMS_TO_TICKS(20));
    fb_clear();    
}

/* ════════════════════════════════════════════════════════════════════════════
   I2C master init
   ════════════════════════════════════════════════════════════════════════════ */

void i2c_master_init(void)
{
    i2c_master_bus_config_t bus_config = {
        .i2c_port              = I2C_NUM_0,
        .sda_io_num            = I2C_MASTER_SDA_IO,
        .scl_io_num            = I2C_MASTER_SCL_IO,
        .clk_source            = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt     = 7,
        .flags.enable_internal_pullup = true,
    };
    ESP_ERROR_CHECK(i2c_new_master_bus(&bus_config, &bus_handle));

    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address  = OLED_I2C_ADDR,
        .scl_speed_hz    = I2C_MASTER_FREQ_HZ,
    };
    ESP_ERROR_CHECK(i2c_master_bus_add_device(bus_handle, &dev_cfg, &oled_handle));
}